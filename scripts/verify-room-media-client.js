"use strict";
const assert = require("node:assert/strict");
const fs = require("node:fs");
const path = require("node:path");
const read = (name) => fs.readFileSync(path.resolve(__dirname, "..", name), "utf8");

const index = read("index.html");
const media = read("js/room-media.js");
const moments = read("js/moments.js");
const promise = read("js/custom-routine.js");
const routine = read("js/sub-routine.js");
const rules = read("storage.rules");

assert.ok(index.indexOf("js/room-media.js") < index.indexOf("js/moments.js"));
assert.match(media, /callable\('executeApprovedRoomDisconnect'\)/);
assert.match(media, /mediaAction: 'uploadRoomMedia'/);
assert.match(media, /mediaAction: 'deleteRoomMedia'/);
assert.match(media, /MAX_SOURCE_BYTES = 12 \* 1024 \* 1024/);
assert.doesNotMatch(moments, /firebase\.storage/);
assert.match(moments, /kind = MEAL_LABELS\[mealType\] \? 'meal' : 'moment'/);
assert.match(moments, /value\.url \|\| value\.dataUrl/);
assert.match(moments, /record\?\.photo/);
assert.match(media, /'routineEntry'/);
assert.match(promise, /photoEnabled: item\.photoEnabled === true/);
assert.match(promise, /class="routine-draft-flags"/);
assert.match(promise, /kind: 'routineEntry'/);
assert.match(promise, /data-custom-photo-file/);
assert.match(promise, /URL\.createObjectURL\(file\)/);
assert.match(promise, /matches\?\.\('\[data-custom-photo-file\]'\)/);
assert.doesNotMatch(promise.match(/function openCustomRoutineHub\(\)[\s\S]*?function closeCustomRoutineHub/)[0], /hmOpenCardConversation/);
assert.match(promise, /const rows = allRows\.filter\(hmCustomAppliesToday\)/);
assert.match(promise, /hmCloseCardConversation\(false\)/);
assert.doesNotMatch(promise, /CustomRoutineCover|customRoutineCover/);
assert.match(routine, /photoEnabled:x\.photoEnabled===true/);
assert.match(routine, /class="routine-draft-flags"/);
assert.match(routine, /kind:'routineEntry'/);
assert.match(routine, /data-sub-photo-file/);
assert.match(routine, /hmPreviewSubRoutinePhotoInput/);
assert.doesNotMatch(routine.match(/function openSubRoutineHub\(\)[\s\S]*?function closeSubRoutineHub/)[0], /hmOpenCardConversation/);
assert.match(routine, /photos,\s*updatedByUid/);
assert.match(routine, /const all=hmSubRoutineRows\(\),rows=hmSubRoutineActiveRows\(\)/);
assert.doesNotMatch(routine, /SubRoutineCover|subRoutineCover|hmSubRoutineImage/);
assert.doesNotMatch(rules, /match \/rooms\//);
assert.match(rules, /fileName == 'avatar\.webp'/);
console.log("Room media client and legacy compatibility verification passed.");
