"use strict";
const assert = require("node:assert/strict");
const fs = require("node:fs");
const { parseImageDataUrl, mediaPath, MAX_BYTES } = require("../functions/room-media");
const tiny = `data:image/webp;base64,${Buffer.from("image").toString("base64")}`;
assert.equal(parseImageDataUrl(tiny).contentType, "image/webp");
assert.equal(mediaPath({ roomCode:"ROOM_1", kind:"promise", resourceId:"card_1", uid:"uid_1" }), "rooms/ROOM_1/media/promise/card_1/cover.webp");
assert.equal(mediaPath({ roomCode:"ROOM_1", kind:"moment", resourceId:"2026-07-24_photo", uid:"uid_1", extension:"jpg" }), "rooms/ROOM_1/media/moment/2026-07-24_photo/uid_1/cover.jpg");
assert.equal(mediaPath({ roomCode:"ROOM_1", kind:"routineEntry", resourceId:"2026-07-24_card_item", uid:"uid_1" }), "rooms/ROOM_1/media/routineEntry/2026-07-24_card_item/uid_1/cover.webp");
assert.throws(() => parseImageDataUrl(`data:image/webp;base64,${Buffer.alloc(MAX_BYTES + 1).toString("base64")}`));
const source = fs.readFileSync(require("node:path").resolve(__dirname, "../functions/index.js"), "utf8");
assert.match(source, /requireActiveRoomMediaAccess/);
assert.match(source, /kind === "promise" && role !== "dom"/);
assert.match(source, /kind === "subRoutine" && role !== "sub"/);
assert.match(source, /request\.data\?\.mediaAction === "uploadRoomMedia"/);
assert.match(source, /request\.data\?\.mediaAction === "deleteRoomMedia"/);
assert.doesNotMatch(source, /exports\.uploadRoomMedia/);
assert.doesNotMatch(source, /exports\.deleteRoomMedia/);
assert.match(source, /allowedPaths = new Set/);
assert.doesNotMatch(source, /requestedPath\.startsWith/);
console.log("Room media server security verification passed.");
